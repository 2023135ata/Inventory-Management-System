class Electronics
{
private:
	int no_of_electronics;
	int types_Electronics;

public:
	
	void set_no_of_electronics(int a)
	{
		no_of_electronics = a;
	}

	void set_types_Electronics(int b)
	{
		types_Electronics = b;
	}

	int get_no_of_electronics()
	{
		return no_of_electronics;
	}

	int get_types_Electronics()
	{
		return types_Electronics;
	}
};
